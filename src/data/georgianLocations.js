export const getLocationsByPrefix = (prefix) => {
  if (!prefix) return GEORGIAN_LOCATIONS.slice(0, 50); // Return first 50 if no prefix
  
  const lowerPrefix = prefix.toLowerCase();
  
  // First: exact prefix matches
  const exactMatches = GEORGIAN_LOCATIONS.filter(loc =>
    loc.toLowerCase().startsWith(lowerPrefix)
  );
  
  // Second: contains matches
  const containsMatches = GEORGIAN_LOCATIONS.filter(loc =>
    loc.toLowerCase().includes(lowerPrefix) &&
    !loc.toLowerCase().startsWith(lowerPrefix)
  );
  
  // Combine and remove duplicates (though our list shouldn't have them)
  return [...new Set([...exactMatches, ...containsMatches])].slice(0, 10);
};